def main():
    # Запрашиваем у пользователя слово
    word = input("Введите слово: ")
    
    # Запрашиваем у пользователя предложение
    sentence = input("Введите предложение: ")
    
    # Подсчитываем количество вхождений слова в предложении
    word_count = sentence.split().count(word)
    
    # Выводим результат
    print(f"Слово '{word}' встречается в предложении {word_count} раз.")

if __name__ == "__main__":
    main()